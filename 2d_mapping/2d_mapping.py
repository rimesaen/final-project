def compute_canvas(image1, warped_corners2):

    h1, w1 = image1.shape[:2]

    corners1 = np.float32(
        [[0,0],
        [w1,0],
        [w1,h1],
        [0,h1]]
    )

    all_corners = np.vstack((corners1, warped_corners2.reshape(-1,2)))

    xmin, ymin = np.int32(all_corners.min(axis=0))
    xmax, ymax = np.int32(all_corners.max(axis=0))

    width = xmax - xmin
    height = ymax - ymin

    T = np.array(
        [[1,0,-xmin],
        [0,1,-ymin],
        [0,0,1]], dtype=np.float32
    )

    return width, height, T

def getMatches(color_image1, color_image2, features, ransac_iterations = 150, dist_threshold = 0.7, image_cen = []):
    # Convert to grayscale for feature detection
    image1_gray = cv2.cvtColor(color_image1, cv2.COLOR_BGR2GRAY)
    image2_gray = cv2.cvtColor(color_image2, cv2.COLOR_BGR2GRAY)

    points1, des1 = features.detectAndCompute(image1_gray,  None)
    points2, des2 = features.detectAndCompute(image2_gray,  None)

    imp = []

    ## Define flann based matcher
    matcher = cv2.FlannBasedMatcher()
    matches = matcher.knnMatch(des1,des2,k=2)

    # important features
    goodmatches = []
    for i, (one, two) in enumerate(matches):
        if one.distance < dist_threshold*two.distance:
            imp.append((one.trainIdx, one.queryIdx))
            goodmatches.append([one])

    matched1 = np.float32([points1[i].pt for (__, i) in imp])
    matched2 = np.float32([points2[i].pt for (i,__) in imp])


    print("len of matched features :", matched1.shape[0])
    # FIX: Use cv2.drawMatchesKnn for goodmatches which is a list of lists
    matches_output = np.zeros((max(color_image1.shape[0], color_image2.shape[0]), color_image1.shape[1] + color_image2.shape[1], 3), dtype=np.uint8)
    drawn = cv2.drawMatchesKnn(color_image1, points1, color_image2, points2, goodmatches, matches_output, flags=cv2.DrawMatchesFlags_NOT_DRAW_SINGLE_POINTS)
    plt.imshow(drawn), plt.show()

    ## to prevent whole pic tilt:
    # Use Affine Partial 2D to reduce tilt
    H_affine, inliers = cv2.estimateAffinePartial2D(matched2, matched1)

    # Handle cases where affine transformation cannot be estimated (e.g., too few matches)
    if H_affine is None:
        print("Warning: Could not estimate Affine Partial 2D. Returning previous panorama.")
        # In a real application, you might want to handle this differently, e.g., raise an error
        # or return a specific error code. For now, using an identity matrix as a fallback.
        H = np.eye(3, dtype=np.float32) # Use identity matrix as a fallback
    else:
        # Convert 2x3 affine matrix to 3x3 homography matrix for perspective transformations
        H = np.vstack([H_affine, np.array([0, 0, 1.0])]) # Use H for consistency with subsequent code

    # Comment out Homography as it's not working well for tilt
    # H, s = cv2.findHomography(matched2, matched1 , cv2.RANSAC, 5.0)

    h1, w1 = color_image1.shape[:2]
    h2, w2 = color_image2.shape[:2]

    # Calculate corners of image2 and warp them
    corners = np.float32([[0,0],[w2,0],[w2,h2],[0,h2]]).reshape(-1,1,2)
    warped_corners = cv2.perspectiveTransform(corners, H)

    # Get the canvas dimensions and translation matrix T
    canvas_width, canvas_height, T_matrix = compute_canvas(color_image1, warped_corners)

    # --- Start of fix for image_cen handling ---
    new_image_cen = []
    if len(image_cen) > 0:
        cleaned_image_cen = []
        for item in image_cen:
            if isinstance(item, tuple) and len(item) == 2:
                cleaned_image_cen.append(item)
            elif isinstance(item, np.ndarray) and item.shape == (1, 1, 2):
                # If it's a 1x1x2 numpy array (like the problematic one), extract the tuple
                cleaned_image_cen.append(tuple(item[0][0]))
            else:
                # Log a warning if an unexpected type is encountered, but try to continue
                print(f"Warning: image_cen contained an unexpected item type/shape: {type(item)}, value: {item}. Skipping.")

        if len(cleaned_image_cen) > 0:
            # Convert list of cleaned tuples to numpy array for perspectiveTransform
            # Ensure consistent float32 type for transformation
            pts_to_transform = np.array(cleaned_image_cen, dtype=np.float32).reshape(-1, 1, 2)
            transformed_existing_pts = cv2.perspectiveTransform(pts_to_transform, T_matrix @ H)
            new_image_cen = [tuple(pt[0]) for pt in transformed_existing_pts] # Convert back to list of tuples

    # Warp image1 with only the translation matrix T_matrix
    # All matched points fit one transformation -- background looks weird
    warped_im1 = cv2.warpPerspective(color_image1, T_matrix, (canvas_width, canvas_height))

    # Warp image2 with H and T_matrix to get its position on the final canvas
    warped_im2 = cv2.warpPerspective(color_image2, T_matrix @ H, (canvas_width, canvas_height))

    # Getting Center of image1 (the newly added image)
    center_new_image_orig = np.array([[[w1/2, h1/2]]], dtype=np.float32)
    transformed_center_new_image = cv2.perspectiveTransform(center_new_image_orig, T_matrix)
    new_image_cen.append(tuple(transformed_center_new_image[0][0])) # Append as tuple

    # --- End of fix for image_cen handling ---

    # Combine the two warped images by taking the maximum pixel values (non-zero merge)
    out = np.maximum(warped_im1, warped_im2)
    # out = np.where(warped_im2 == 0, warped_im2, np.maximum(warped_im1, warped_im2))
    final_out = getNonZeroImage(out)

    return final_out, out , des1[0], new_image_cen # Return the updated image_cen

def getNonZeroImage(image):

    ## keep nonzero pixels
    out_gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    pixels = cv2.findNonZero(out_gray)
    pixels = pixels.reshape([pixels.shape[0],2])

    extreme_min_x = min(pixels[:,0])
    extreme_max_x = max(pixels[:,0])
    extreme_min_y = min(pixels[:,1])
    extreme_max_y = max(pixels[:,1])

    ## clip with extremes
    print("ys :",extreme_min_y, extreme_max_y)
    print("xs :",extreme_min_x, extreme_max_x)

    image = image[extreme_min_y:extreme_max_y, extreme_min_x:extreme_max_x,:]
    return image

