def plot_image_list(images, cols=4):
    n_images = len(images)
    rows = (n_images // cols) + (1 if n_images % cols > 0 else 0)

    # Create the figure and axes
    fig, axes = plt.subplots(rows, cols, figsize=(15, 4 * rows))

    # Flatten axes for easy iteration if it's a 2D array
    axes = axes.flatten() if n_images > 1 else [axes]

    for i in range(n_images):
        axes[i].imshow(images[i])
        axes[i].axis('off')  # Hide pixel coordinates for a cleaner look

    # Remove any extra empty subplots
    for j in range(i + 1, len(axes)):
        axes[j].axis('off')

    plt.tight_layout() # Minimize space between images
    plt.show()

